// 获取当前地区
export function getRegion(settings) {
    if (settings) {
        const region = settings.get_string('festival-region');
        if (region && region !== 'auto') {
            return region;
        }
    }
    const lang = GLib.getenv('LANG') || '';
    if (lang.startsWith('zh_TW')) return 'TW';
    if (lang.startsWith('zh_HK')) return 'HK';
    return 'CN';
}